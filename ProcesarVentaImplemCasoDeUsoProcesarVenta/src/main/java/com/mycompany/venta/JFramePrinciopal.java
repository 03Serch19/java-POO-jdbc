    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        idArticulo=JFramePrinciopal.this.jTeIdArti.getText();
        cantidad=Integer.parseInt(JFramePrinciopal.this.jTeCant.getText());
       if(cpv.tienda.catalogo.validarProductoCatalogo(idArticulo)){
        if(jTable1.getRowCount()==0){
            //primer item del JTable(Grid)
            if(cantidad>0)AgregaItemInterfaz(1);
            else{JOptionPane.showMessageDialog(null, "Error, introduzca una cantidad");
            }
        }else{
            //Del segundo item del JTable(Grid) en Adelante
            if(cantidad>0){
                if(cpv.venta.validarItem(idArticulo)==false){
                    AgregaItemInterfaz(2);
                }
                else{JOptionPane.showMessageDialog(null, "Articulo ya registrado en la venta");}
            }
            else{JOptionPane.showMessageDialog(null, "Error, introduzca una cantidad");
            }
        }
       }else{
           JOptionPane.showMessageDialog(null, "Error, codigo no existe");
       } 
    }//GEN-LAST:event_jButton2ActionPerformed

